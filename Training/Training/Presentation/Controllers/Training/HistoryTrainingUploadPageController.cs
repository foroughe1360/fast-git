        // GET: HistoryTrainingUploadPage
        public ActionResult Index()
        {
            try
            {
                var list = _HistoryTrainingUploadPageProvider.GetAll().ToList();
                return View(list);
            }
            catch (Exception e)
            {
                LogErrorEntity logerrorentity = new LogErrorEntity((int)TableInterfaceValueEntity.Form.HistoryTrainingUploadPage, e.Message, (int)TableInterfaceValueEntity.OperationType.Index, "");
                _LogErrorProvider.Add(logerrorentity);
                return RedirectToAction("PageError", "Home", new { ErrorMessage = e.Message });
            }
        }

        //Get: Create HistoryTrainingUploadPage Page
        public ActionResult Create()
        {
            try
            {
                return View();
            }
            catch (Exception e)
            {
                LogErrorEntity logerrorentity = new LogErrorEntity((int)TableInterfaceValueEntity.Form.HistoryTrainingUploadPage, e.Message, (int)TableInterfaceValueEntity.OperationType.CreateGet, "");
