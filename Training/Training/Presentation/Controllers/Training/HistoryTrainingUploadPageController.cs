using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.Mvc;

namespace Presentation.Controllers
{
    [CustomAuthorize(Roles = "Admin,User", RolesConfigKey = "29")]
    public class HistoryTrainingUploadPageController : Controller
    {
        private HistoryTrainingUploadPageProvider _HistoryTrainingUploadPageProvider;
        private LogErrorProvider _LogErrorProvider;
        private OperationLogProvider _OperationLogProvider;

        public HistoryTrainingUploadPageController()
        {
            _HistoryTrainingUploadPageProvider = new HistoryTrainingUploadPageProvider();
