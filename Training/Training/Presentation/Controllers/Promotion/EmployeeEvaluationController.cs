using Bussiness;
using InterfaceEntity;
using Presentation.Utility;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.Mvc;

namespace Presentation.Controllers
{
    [CustomAuthorize(Roles = "Admin")]
    public class EmployeeEvaluationController : Controller
    {
        private EmployeeEvaluationProvider _EmployeeEvaluationProvider;
        private LogErrorProvider _LogErrorProvider;

        public EmployeeEvaluationController()
        {
            _EmployeeEvaluationProvider = new EmployeeEvaluationProvider();
            _LogErrorProvider = new LogErrorProvider();
        }

        // GET: EmployeeEvaluation List
        public ActionResult Index()
        {
            try
            {
                var list = _EmployeeEvaluationProvider.GetAll().ToList();
                return View(list);
            }
            catch (Exception e)
            {
                LogErrorEntity logerrorentity = new LogErrorEntity((int)TableInterfaceValueEntity.Form.EmployeeEvaluation, e.Message, (int)TableInterfaceValueEntity.OperationType.Index, "");
                _LogErrorProvider.Add(logerrorentity);
                return RedirectToAction("PageError", "Home", new { ErrorMessage = e.Message });
            }
        }

        //Get: EmployeeEvaluation Page
        public ActionResult Create()
        {
